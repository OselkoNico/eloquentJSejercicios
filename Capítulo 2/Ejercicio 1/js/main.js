for (let numero = 1; numero <= 7; numero++) {
    if (numero === 1) {
        console.log("#");
    }else if (numero === 2) {
        console.log("##");
    }else if (numero === 3) {
        console.log("###");
    }else if (numero === 4) {
        console.log("####");
    }else if (numero === 5) {
        console.log("#####");
    }else if (numero === 6) {
        console.log("######");
    }else if (numero === 7) {
        console.log("#######");
    }
}